function PlanoDinMHf14orden
%Dibuja el plano dinámico del método (MH4O) para la función f_1(x)

li=-3;ls=3;%Definimos los límites para los intervalos
N=500; maxiter=80; tol=1e-6;
x=linspace(li,ls,N); y=x;
[X,Y]=meshgrid(x,y);
z=X+1i*Y;

%Raíces del polinomio
raices=roots([1 4 0 -10]);

%Operador de punto fijo
R=@(z)(3400000 + 21760000*z + 52496000*z^2 + 43332000*z^3 - 40492800*z^4 - ...
   103067200*z^5 + 33255480*z^6 + 113620*z^14 + 7787*z^15 + 3*z^13*(224608 +... 
   729*sqrt(3)*sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))) + 1024*z^7*(226205 + 768*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))) + 8*z^12*(299641 + 4374*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))) + 32*z^11*(274657 + 7290*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))) + 160*z^9*(797855 + 10368*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))) + 32*z^10*(1212241 + 25920*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))) + 64*z^8*(3831817 + 27648*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + ...
392*z^5 + 49*z^6)/(z*(8 + 3*z)))))/(3*z^4*(8 + 3*z)^4*...
(400 + 1280*z + 1040*z^5 + 130*z^6 + 32*z^2*(35 + 2*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + 392*z^5 +... 
49*z^6)/(z*(8 + 3*z)))) + 8*z^3*(355 + 6*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + 392*z^5 + ...
49*z^6)/(z*(8 + 3*z)))) + z^4*(2816 + 9*sqrt(3)*...
sqrt((400 + 1280*z + 1120*z^2 + 1304*z^3 + 1088*z^4 + 392*z^5 + ...
49*z^6)/(z*(8 + 3*z))))));
  
for j=1:N
    for k=1:N
        z0=z(j,k);
        z0=R(z0); iter=1; dif=tol+1;
        while and(iter<maxiter,dif>tol)
            z0=R(z0);
            dif1=abs(z0-raices(1)); dif2=abs(z0-raices(2));
            dif3=abs(z0-raices(3));
            dif=min([dif1,dif2,dif3]);
            iter=iter+1;
        end
        y(j,k)=z0;
        IT(j,k)=iter;
    end
end
p=polyfit([min(IT(:)) max(IT(:))],[1 .25],1);
cIT=polyval(p,IT);

Z1=abs(y-raices(1))<tol;
Z2=abs(y-raices(2))<tol;
Z3=abs(y-raices(3))<tol;

I(:,:,1)=cIT.*(237/255*Z1+3/255*Z3+100/255*Z2);
I(:,:,2)=cIT.*(85/255*Z1+102/255*Z2+200/255*Z3);
I(:,:,3)=cIT.*(70/225*Z1+205/255*Z2+79/255*Z3);

figure, imshow(double(I),'XData',[li ls],'YData',[li ls]), axis on, axis xy
hold on
plot(real(raices),imag(raices),'w*'), xlabel('Real(z)'),...
    ylabel('Imag(z)'), grid
end

